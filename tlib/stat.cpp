
#include <glib_defines.h>
#include "stat.h"

/****************************************************************************

****************************************************************************/

STAT::STAT(void)  {
	reset();
	return;
}

/****************************************************************************

****************************************************************************/


STAT::~STAT()  {

}

/****************************************************************************

****************************************************************************/

void STAT::reset(void)  {
	total = 0.0;
	count = 0;
	avg = 0.0;
	min = 0.0;
	max = 0.0;
	return;
}

/****************************************************************************

****************************************************************************/

void STAT::update(double _val) {
	total += _val;
	count++;
	avg = total / count;
	min = MIN(_val, min);
	max = MAX(_val, max);

	return;
}

