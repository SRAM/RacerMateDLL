#include "data.h"

/**********************************************************************

**********************************************************************/

DATA::DATA(void) {
	init();
}

/**********************************************************************

**********************************************************************/

void DATA::init(void) {
	reset();
	return;
}

/**********************************************************************

**********************************************************************/

void DATA::reset(void) {
	mph = 0.0f;
	watts = 0.0f;
	rpm = 0.0f;
	lata = 90.0f;
	rata = 90.0f;
	miles = 0.0f;
	ss = 0.0f;
	lss = 0.0f;
	rss = 0.0f;
	lwatts = 0.0f;
	rwatts = 0.0f;
	hr = 0.0f;
	cals = 0.0f;

	hrflags = 0;

	return;
}


